	private void countScores(String choice, int AI){
		if(choice!=null){
			switch (choice){
				case "A":
					if(AI==1){
						totalA1++;
					}else if(AI==2){
						totalA2++;
					}
					break;
				case "B":
					if(AI==1){
						totalB1++;
					}else if(AI==2){
						totalB2++;
					}
					break;
				case "C":
					if(AI==1){
						totalC1++;
					}else if(AI==2){
						totalC2++;
					}
					break;
				case "D":
					if(AI==1){
						totalD1++;
					}else if(AI==2){
						totalD2++;
					}
					break;
				case "E":
					if(AI==1){
						totalE1++;
					}else if(AI==2){
						totalE2++;
					}
					break;
				case "N":
					if(AI==1){
						totalN1++;
					}else if(AI==2){
						totalN2++;
					}
					break;
			}
		}
	}
	private void analyzeForm(Form form, int AI){
		if(form!=null){
			countScores(form.getEvenness(),AI);
			countScores(form.getFall(),AI);
			countScores(form.getFinish(),AI);
			countScores(form.getJointwidth(),AI);
			countScores(form.getLevel(),AI);
			countScores(form.getLintol(),AI);
			countScores(form.getPlumb(),AI);
			countScores(form.getSoundness(),AI);
		}
	}
	public void calculateScores(){

		double factorScores = 0;
		int AI = 1;
		analyzeForm(getForm1(),AI);
		analyzeForm(getForm2(),AI);
		analyzeForm(getForm3(),AI);
		analyzeForm(getForm4(),AI);
		analyzeForm(getForm21(),AI);
		analyzeForm(getForm22(),AI);
		analyzeForm(getForm23(),AI);
		analyzeForm(getForm24(),AI);
		analyzeForm(getForm31(),AI);
		analyzeForm(getForm32(),AI);
		analyzeForm(getForm33(),AI);
		analyzeForm(getForm34(),AI);
		AI = 2;
		analyzeForm(getForm41(),AI);
		analyzeForm(getForm42(),AI);
		analyzeForm(getForm43(),AI);
		analyzeForm(getForm44(),AI);
		analyzeForm(getForm51(),AI);
		analyzeForm(getForm52(),AI);
		analyzeForm(getForm53(),AI);
		analyzeForm(getForm54(),AI);

		setAllotedMark1(3);
		setAllotedMark2(3);

		try{
			factorScores = (((3*getTotalA1() + 2*getTotalB1() + getTotalC1()) / (4*(getTotalA1() + getTotalB1() + getTotalC1() + getTotalD1() + getTotalE1()))) * getAllotedMark1()) - 0.3;
		}catch(Exception e){
			factorScores = 0;
		}
		setFactorScore1(factorScores);
		try{
			factorScores = (((3*getTotalA2() + 2*getTotalB2() + getTotalC2()) / (4*(getTotalA2() + getTotalB2() + getTotalC2() + getTotalD2() + getTotalE2()))) * getAllotedMark2()) - 0.3;
		}catch(Exception e){
			factorScores = 0;
		}
		setFactorScore2(factorScores);


		setTotalAllotedMark(getAllotedMark1() + getAllotedMark2());
		setTotalFactorScore(getFactorScore1() + getFactorScore2());

		setTotalAIScore(getTotalFactorScore() / getTotalAllotedMark());
		System.out.println("Computations done!");
	}



