package com.accenture.hkha.model;

public class Form {
	
	private String soundness;
	private String evenness;
	private String level;
	private String fall;

	public String getSoundness() {
		return soundness;
	}

	public void setSoundness(String soundness) {
		this.soundness = soundness;
	}

	public String getEvenness() {
		return evenness;
	}

	public void setEvenness(String evenness) {
		this.evenness = evenness;
	}

	public String getLevel() {
		return level;
	}

	public void setLevel(String level) {
		this.level = level;
	}

	public String getFall() {
		return fall;
	}

	public void setFall(String fall) {
		this.fall = fall;
	}

}
