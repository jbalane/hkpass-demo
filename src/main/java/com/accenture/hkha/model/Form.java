package com.accenture.hkha.model;

public class Form {

	private String soundness;
	private String evenness;
	private String level;
	private String fall;


	//other points
	private String lintol;
	private String jointwidth;
	private String finish;
	private String plumb;

	public String getLintol() {
		return lintol;
	}

	public void setLintol(String lintol) {
		this.lintol = lintol;
	}

	public String getJointwidth() {
		return jointwidth;
	}

	public void setJointwidth(String jointwidth) {
		this.jointwidth = jointwidth;
	}

	public String getFinish() {
		return finish;
	}

	public void setFinish(String finish) {
		this.finish = finish;
	}

	public String getPlumb() {
		return plumb;
	}

	public void setPlumb(String plumb) {
		this.plumb = plumb;
	}

	public String getSoundness() {
		return soundness;
	}

	public void setSoundness(String soundness) {
		this.soundness = soundness;
	}

	public String getEvenness() {
		return evenness;
	}

	public void setEvenness(String evenness) {
		this.evenness = evenness;
	}

	public String getLevel() {
		return level;
	}

	public void setLevel(String level) {
		this.level = level;
	}

	public String getFall() {
		return fall;
	}

	public void setFall(String fall) {
		this.fall = fall;
	}

}
