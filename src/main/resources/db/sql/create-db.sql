CREATE TABLE WORK (
	id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 100, INCREMENT BY 1) PRIMARY KEY,
	status VARCHAR(30),
	created_date VARCHAR(30),
	created_by VARCHAR(30)
);

CREATE TABLE SCORES (
	id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 200, INCREMENT BY 1) PRIMARY KEY,
	work_id INTEGER,
	spot INTEGER,
	soundness VARCHAR(3),
	evenness VARCHAR(3),
	level VARCHAR(3),
	fall VARCHAR(3)
);

CREATE  TABLE users (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 200, INCREMENT BY 1) PRIMARY KEY,
  username VARCHAR(45),
  password VARCHAR(45),
  enabled INTEGER
);

CREATE TABLE user_roles (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 200, INCREMENT BY 1) PRIMARY KEY,
  username varchar(45),
  role varchar(45)
  );
